#This code adds percentage changes to the code of absolute changes, and divides it in two separate csv tables.
import pandas as pd
import numpy as np

df20= pd.read_csv("df_new20.csv", index_col=0)
dfdelta= pd.read_csv("df_newdelta.csv", index_col="GeoName")

# display(df20.head(2))
# display(dfdelta.head(2))

totdf = dfdelta[['GDP', 'PCE', 'PersonalIncome', 'RPP', 'JobsNumber',
       'population', 'Jobs/Population', 'CGDP','CDPI', 'CPI','CPCE']]

# totdf.index= dfdelta.index

#Adding percentage changes:  (2023-2020)/(2020)
totdf["%Pop_Ch"] = totdf.loc[:,"population"]/df20.loc[:,"population"]
totdf["%PI_Ch"] = totdf.loc[:,"PersonalIncome"]/df20.loc[:,"PersonalIncome"]
totdf["%GDP_Ch"] = totdf.loc[:,"GDP"]/df20.loc[:,"GDP"]
totdf["%Jobs_Ch"] = totdf.loc[:,"JobsNumber"]/df20.loc[:,"JobsNumber"]
totdf["%PCE_Ch"] = totdf.loc[:,"PCE"]/df20.loc[:,"PCE"]

totdf["%CDPI_Ch"] = totdf.loc[:,"CDPI"]/df20.loc[:,"CDPI"]
totdf["%CPI_Ch"] = totdf.loc[:,"CPI"]/df20.loc[:,"CPI"]
totdf["%CGDP_Ch"] = totdf.loc[:,"CGDP"]/df20.loc[:,"CGDP"]
totdf["%CPCE_Ch"] = totdf.loc[:,"CPCE"]/df20.loc[:,"CPCE"]

totdf.head(5)



list(totdf.columns)
totdelta = totdf.loc(axis=1)[['GDP','PCE','PersonalIncome','JobsNumber','population','Jobs/Population','CGDP','CDPI','CPI','CPCE']]
pctdelta = totdf.loc(axis=1)[['RPP','%Pop_Ch','%PI_Ch','%GDP_Ch','%Jobs_Ch','%PCE_Ch','%CDPI_Ch','%CPI_Ch','%CGDP_Ch','%CPCE_Ch']]

