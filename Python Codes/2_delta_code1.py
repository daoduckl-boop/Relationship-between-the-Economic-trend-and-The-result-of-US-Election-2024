#Ths code is to add some columns of economic data to our previously cleaned table.
import pandas as pd
import numpy as np

df_2020= pd.read_csv("df_2020-2.csv")
df_2023= pd.read_csv("df_2023-2.csv")
population= pd.read_csv("population.csv")
tab20= pd.read_csv("Table2020.csv")
tab23= pd.read_csv("Table2023.csv")

df_2023.rename(columns={"  Personal income":"PersonalIncome", "No_employment":"JobsNumber"}, inplace = True)
df_2020.rename(columns={"  Personal income":"PersonalIncome", "No_employment":"JobsNumber"}, inplace = True)

df_2023.set_index("GeoName", inplace=True)
df_2020.set_index("GeoName", inplace=True)

population.set_index("States", inplace=True)

df23 = pd.concat([df_2023, population['2023']], axis=1)
df23.rename(columns={"2023":"population"}, inplace=True)
df23["Jobs/Population"]= df23["JobsNumber"]/df23["population"]
df23["CGDP"]=(df23["GDP"]/df23["population"])*1000000
df23["CGDP"]=df23["CGDP"].astype(int)

df20 = pd.concat([df_2020, population['2020']], axis=1)
df20.rename(columns={"2020":"population"}, inplace=True)
df20["Jobs/Population"]= df20["JobsNumber"]/df20["population"]
df20["CGDP"]=(df20["GDP"]/df20["population"])*1000000
df20["CGDP"]=df20["CGDP"].astype(int)

df23.index.names = ["GeoName"]
df20.index.names = ["GeoName"]
df23.index = df23.index.str.upper()
df20.index = df20.index.str.upper()

tab20.dropna(inplace=True)
tab20.set_index(tab20.iloc[:,0], inplace=True)
tab20.drop(tab20.columns[[0,1]], axis=1, inplace=True)
tab20 = tab20[tab20["Description"]=="  Per capita personal consumption expenditures (PCE) 8"]
tab20.index = tab20.index.str.upper()

tab20[["2020"]]= tab20[["2020"]].astype(int)
tab20.drop(tab20[["Description"]], axis=1, inplace=True)
tab20.columns=["CPCE"]

tab23.dropna(inplace=True)
tab23.set_index(tab23.iloc[:,0], inplace=True)
tab23.drop(tab23.columns[[0,1]], axis=1, inplace=True)
tab23 = tab23[tab23["Description"]=="  Per capita personal consumption expenditures (PCE) 8"]
tab23.index = tab23.index.str.upper()

tab23[["2023"]]= tab23[["2023"]].astype(int)
tab23.drop(tab23[["Description"]], axis=1, inplace=True)
tab23.columns=["CPCE"]

df23 = pd.concat([df23, tab23], axis=1)
df20 = pd.concat([df20, tab20], axis=1)

df_delta = df23-df20
df_delta=df_delta.drop(columns='year')

#Download the delta change file
from google.colab import files
df_delta.to_csv('df_newdelta.csv',index=True)
files.download('df_newdelta.csv')

#Download the dataframes of economic data of 2020 and 2023
df20.to_csv('df_new20.csv',index=True)
files.download('df_new20.csv')
df23.to_csv('df_new23.csv',index=True)
files.download('df_new23.csv')