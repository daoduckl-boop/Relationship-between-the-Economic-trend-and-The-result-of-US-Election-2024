#This code is to get a table for the difference between 2023 and 2019 for population and jobs data.
import pandas as pd
import numpy as np

df23= pd.read_csv("df_new23.csv", index_col=0)
tab19= pd.read_csv("Table2019.csv",skiprows=3)
popp = pd.read_csv("Pop_Est_2019.csv", skiprows=1)

popp = popp[0:52]
popp.iloc(axis=1)[0] = popp.iloc(axis=1)[0].map(lambda x: x.strip().replace(".", ""))

popp.set_index(popp.iloc(axis=1)[0], inplace=True)
pop19= popp['2019']

pop19=pop19.apply(lambda x: x.replace(",","")).astype(int)
pop19.index.name = "States"

tab19.dropna(inplace=True)
tab19.set_index(tab19.iloc[:,0], inplace=True)
tab19.drop(tab19.columns[[0,1]], axis=1, inplace=True)
jobs19 = tab19[tab19.iloc(axis=1)[0]== "  Total employment (number of jobs)"]

jobs19.drop(jobs19.columns[0], axis=1, inplace=True)
jobs19.index.name="States"
jobs19.iloc(axis=1)[0] = jobs19.iloc(axis=1)[0].astype(int)

pop19.index = pop19.index.str.upper()
jobs19.index = jobs19.index.str.upper()

df19 = pd.concat([jobs19, pop19], axis=1)
df19.columns = ["JobsNumber", "population"]

df23.index = df23.index.str.upper()

df19["Jobs/Population"] = df19["JobsNumber"]/df19["population"]
dif23_19 = (df23-df19).dropna(axis=1)

dif23_19.head(2)

#Adding the change expressed in percentage...
dif23_19["%Jobs_Ch"] = dif23_19.loc[:,"JobsNumber"]/df19.loc[:,"JobsNumber"]
dif23_19["%Pop_Ch"] = dif23_19.loc[:,"population"]/df19.loc[:,"population"]

dif23_19 = dif23_19.applymap(lambda x: round(x, 4))

from google.colab import files
dif23_19.to_csv("difference23_19.csv",index=True)
files.download('difference23_19.csv')

df19.to_csv("df_new19.csv",index=True)
files.download('df_new19.csv')