#This code is to clean the Population Esitmates csv file.
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import plotly.express as px

popp = pd.read_csv("Population_Estimates.csv")

popp.iloc[:,0] = popp.iloc[:,0].map(lambda x: x.strip().replace(".", ""))
column1 = popp.iloc[:,0]
popp = popp.drop(popp.columns[[0,1]], axis=1).applymap(lambda x: x.replace(",","")).astype(int)
popp = pd.concat([column1, popp], axis=1)
popp.rename(columns={'Unnamed: 0':"States"}, inplace=True)

popp.to_csv('population.csv',index=False)